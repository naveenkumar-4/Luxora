import mongoose from "mongoose"; // Erase if already required

// Declare the Schema of the Mongo model
export const userSchema = new mongoose.Schema({
    firstname:{
        type:String,
        required:true,
    },
    lastname:{
        type:String,
        required:true,
    },
    email:{
        type:String,
        required:true,
        unique:true,
    },
    mobile:{
        type:String,
        required:true,
        unique:true,
    },
    password:{
        type:String,
        required:true,
    },
});

export const UserSchema = mongoose.model('User', userSchema)

